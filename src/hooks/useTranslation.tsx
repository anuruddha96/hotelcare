type Language = keyof typeof translations;
type TranslationKey = keyof typeof translations.en;

const supportedLanguages: Language[] = ['en', 'hu', 'es', 'vi', 'mn', 'az', 'tl', 'uk'];
const LANGUAGE_STORAGE_KEYS = ['preferred_language', 'preferred-language'] as const;
const TRANSLATION_BUNDLE_CACHE_PREFIX = 'translation_bundle_';

const isLanguage = (value: string | null): value is Language =>
  !!value && supportedLanguages.includes(value as Language);

const getStoredLanguage = (): Language | null => {
  for (const key of LANGUAGE_STORAGE_KEYS) {
    const stored = localStorage.getItem(key);
    if (isLanguage(stored)) return stored;
  }

  return null;
};

const persistLanguage = (lang: Language) => {
  LANGUAGE_STORAGE_KEYS.forEach((key) => localStorage.setItem(key, lang));
};

// Recursively flatten nested translation objects so callers can use dotted keys
// like `t('housekeeping.priority.medium')` regardless of whether the source
// module stored that value as a flat key or as a nested object.
const flattenBundle = (
  source: Record<string, unknown> | undefined,
  prefix = '',
  out: Record<string, string> = {},
): Record<string, string> => {
  if (!source) return out;
  for (const [key, value] of Object.entries(source)) {
    const next = prefix ? `${prefix}.${key}` : key;
    if (value && typeof value === 'object' && !Array.isArray(value)) {
      flattenBundle(value as Record<string, unknown>, next, out);
    } else if (typeof value === 'string') {
      out[next] = value;
    }
  }
  return out;
};

const getStaticTranslationBundle = (lang: Language): Record<string, string> => ({
  ...flattenBundle(translations[lang] as Record<string, unknown> | undefined),
  ...flattenBundle(additionalTranslations[lang] as Record<string, unknown> | undefined),
  ...flattenBundle(expandedTranslations[lang] as Record<string, unknown> | undefined),
  ...flattenBundle(notificationTranslations[lang as keyof typeof notificationTranslations] as Record<string, unknown> | undefined),
  ...flattenBundle(dashboardTranslations[lang as keyof typeof dashboardTranslations] as Record<string, unknown> | undefined),
  ...flattenBundle(pmsTranslations[lang] as Record<string, unknown> | undefined),
  ...flattenBundle(highlightedTranslations[lang] as Record<string, unknown> | undefined),
  ...flattenBundle(screenTranslations[lang] as Record<string, unknown> | undefined),
  ...flattenBundle(roomOverviewTranslations[lang as keyof typeof roomOverviewTranslations] as Record<string, unknown> | undefined),
  ...flattenBundle((purchaseInvoiceTranslations as any)[lang] as Record<string, unknown> | undefined),
  ...flattenBundle(locationTranslations[lang] as Record<string, unknown> | undefined),
});

const getCachedTranslationBundle = (lang: Language): Record<string, string> => {
  try {
    const cached = localStorage.getItem(`${TRANSLATION_BUNDLE_CACHE_PREFIX}${lang}`);
    return cached ? JSON.parse(cached) : {};
  } catch {
    return {};
  }
};

const persistTranslationBundle = (lang: Language, bundle: Record<string, string>) => {
  try {
    localStorage.setItem(`${TRANSLATION_BUNDLE_CACHE_PREFIX}${lang}`, JSON.stringify(bundle));
  } catch {
    // Ignore quota/private-mode failures; static translations still render synchronously.
  }
};

interface TranslationContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: TranslationKey | string) => string;
}

const TranslationContext = createContext<TranslationContextType | null>(null);

export function TranslationProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    const stored = getStoredLanguage();
    if (stored) return stored;
    
    const browserLang = navigator.language?.split('-')[0]?.toLowerCase();
    const detected = supportedLanguages.find(l => l === browserLang);
    return detected || 'en';
  });

  const [cachedBundles] = useState<Record<Language, Record<string, string>>>(() =>
    supportedLanguages.reduce((bundles, lang) => {
      bundles[lang] = getCachedTranslationBundle(lang);
      return bundles;
    }, {} as Record<Language, Record<string, string>>)
  );

  // Load custom translations from localStorage
  const [customTranslations, setCustomTranslations] = useState<any>(() => {
    try {
      const stored = localStorage.getItem('custom_translations');
      return stored ? JSON.parse(stored) : {};
    } catch {
      return {};
    }
  });

  const activeBundle = useMemo(() => {
    const customBundle = Object.entries(customTranslations).reduce((bundle, [key, values]: [string, any]) => {
      if (values?.[language] && values[language] !== key) bundle[key] = values[language];
      return bundle;
    }, {} as Record<string, string>);

    return {
      ...cachedBundles[language],
      ...getStaticTranslationBundle(language),
      ...customBundle,
    };
  }, [cachedBundles, customTranslations, language]);

  const englishBundle = useMemo(() => ({
    ...cachedBundles.en,
    ...getStaticTranslationBundle('en'),
  }), [cachedBundles]);

  useEffect(() => {
    persistTranslationBundle(language, activeBundle);
  }, [activeBundle, language]);

  const t = (key: TranslationKey | string): string => {
    const translated = activeBundle[key];
    if (translated && translated !== key) return translated;

    const english = englishBundle[key];
    if (english && english !== key) return english;

    return key;
  };

  const changeLanguage = (lang: Language) => {
    setLanguage(lang);
    persistLanguage(lang);
  };

  return (
    <TranslationContext.Provider value={{ language, setLanguage: changeLanguage, t }}>
      {children}
    </TranslationContext.Provider>
  );
}

export const useTranslation = () => {
  const context = useContext(TranslationContext);
  if (!context) {
    // Return a fallback object instead of throwing an error during development
    console.warn('useTranslation must be used within a TranslationProvider');
    return {
      language: 'en' as const,
      setLanguage: () => {},
      t: (key: string) => {
        // Extract the readable part from translation keys
        const parts = key.split('.');
        return parts[parts.length - 1].replace(/([A-Z])/g, ' $1').replace(/^./, str => str.toUpperCase());
      },
    };
  }
  return context;
};
